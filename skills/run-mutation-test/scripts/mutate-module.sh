#!/usr/bin/env bash
# mutate-module.sh
# Runs Stryker.js mutation testing on a specific source module.
# Enforces the 250-line pre-check before running.
#
# Usage:
#   bash .claude/skills/run-mutation-test/scripts/mutate-module.sh <path-to-module>
#   bash .claude/skills/run-mutation-test/scripts/mutate-module.sh src/domain/pricing.js
#
# Exit codes:
#   0 — mutation run complete (check score in output)
#   1 — pre-check failed or Stryker not installed
#   2 — module over 250 lines (must split first)

set -euo pipefail

MODULE="${1:-}"

# ─── Validate input ───────────────────────────────────────────────────────────
if [ -z "$MODULE" ]; then
  echo "Usage: bash mutate-module.sh <path-to-module>"
  echo "Example: bash mutate-module.sh src/domain/pricing.js"
  exit 1
fi

if [ ! -f "$MODULE" ]; then
  echo "ERROR: Module not found: $MODULE"
  exit 1
fi

# ─── Pre-check: module size ───────────────────────────────────────────────────
LINE_COUNT=$(wc -l < "$MODULE")

if [ "$LINE_COUNT" -gt 250 ]; then
  echo ""
  echo "╔═══════════════════════════════════════════════════════════╗"
  echo "║  MODULE TOO LARGE — CANNOT RUN MUTATION TESTS             ║"
  echo "╠═══════════════════════════════════════════════════════════╣"
  echo "║  File  : $MODULE"
  echo "║  Lines : $LINE_COUNT (limit: 250)"
  echo "║                                                           ║"
  echo "║  Per CLAUDE.md coding constraints, modules over 250 lines ║"
  echo "║  must be split before mutation testing.                   ║"
  echo "║                                                           ║"
  echo "║  Next step: run the guide-refactor skill on this module.  ║"
  echo "╚═══════════════════════════════════════════════════════════╝"
  echo ""
  exit 2
fi

echo ""
echo "Module: $MODULE ($LINE_COUNT lines) ✓"
echo ""

# ─── Check Stryker is installed ───────────────────────────────────────────────
if ! npx --no-install stryker --version > /dev/null 2>&1; then
  echo "ERROR: @stryker-mutator/core is not installed."
  echo ""
  echo "Install with:"
  echo "  npm install --save-dev @stryker-mutator/core @stryker-mutator/jest-runner"
  exit 1
fi

# ─── Snapshot node_modules packages Stryker is known to perturb ──────────────
# Stryker copies the project into a sandbox and runs `npm install` there.
# In monorepos with workspace hoisting, this can silently upgrade packages
# in the root/app-level node_modules (e.g. @sinonjs/fake-timers, umzug/glob).
# We take a shallow snapshot before and restore it unconditionally on exit.
SNAPSHOT_DIR=$(mktemp -d)

snapshot_package() {
  local pkg_path="$1"
  local snapshot_name="$2"
  if [ -d "$pkg_path" ]; then
    cp -r "$pkg_path" "$SNAPSHOT_DIR/$snapshot_name"
  fi
}

# Packages that have been observed to be corrupted by Stryker sandbox installs:
snapshot_package "node_modules/@sinonjs/fake-timers"      "sinonjs-fake-timers"
snapshot_package "node_modules/@sinonjs/commons"           "sinonjs-commons"
snapshot_package "node_modules/umzug/node_modules/glob"    "umzug-glob"

restore_snapshots() {
  restore_package() {
    local pkg_path="$1"
    local snapshot_name="$2"
    if [ -d "$SNAPSHOT_DIR/$snapshot_name" ]; then
      rm -rf "$pkg_path"
      cp -r  "$SNAPSHOT_DIR/$snapshot_name" "$pkg_path"
    fi
  }

  restore_package "node_modules/@sinonjs/fake-timers"      "sinonjs-fake-timers"
  restore_package "node_modules/@sinonjs/commons"           "sinonjs-commons"
  restore_package "node_modules/umzug/node_modules/glob"    "umzug-glob"

  rm -rf "$SNAPSHOT_DIR"

  # Belt-and-suspenders: remove any lingering sandbox dirs (cleanTempDir: true
  # inside Stryker is sometimes skipped when the run is interrupted).
  rm -rf .stryker-tmp
}

# Restore on any exit — success, failure, or signal
trap restore_snapshots EXIT

# ─── Build a focused temp config ─────────────────────────────────────────────
TEMP_CONFIG=".stryker-temp-$(basename "$MODULE" | tr '.' '-').mjs"
REPORT_DIR="reports/mutation"

mkdir -p "$REPORT_DIR"

cat > "$TEMP_CONFIG" << STRYKER_EOF
// Temporary Stryker config — auto-generated by mutate-module.sh
// Target module: ${MODULE}
// Generated: $(date -u +"%Y-%m-%dT%H:%M:%SZ")
// DO NOT COMMIT THIS FILE

export default {
  testRunner: 'jest',
  mutate: ['${MODULE}'],
  jest: {
    projectType: 'node',
    enableFindRelatedTests: true,
  },
  reporters: ['progress', 'clear-text', 'json'],
  jsonReporter: {
    fileName: '${REPORT_DIR}/mutation-$(basename "$MODULE" | sed "s/\\..*//").json',
  },
  thresholds: {
    high: 80,
    low:  60,
    break: null,
  },
  tempDirName: '.stryker-tmp',
  cleanTempDir: true,
  // symlinkNodeModules: symlink instead of copying node_modules into each sandbox.
  // This prevents Stryker from running `npm install` inside the sandbox, which
  // would corrupt hoisted package versions in the workspace root node_modules.
  symlinkNodeModules: true,
  coverageAnalysis: 'perTest',
  logLevel: 'info',
};
STRYKER_EOF

echo "Running Stryker on: $MODULE"
echo "This will take a few minutes..."
echo ""

# ─── Run Stryker ─────────────────────────────────────────────────────────────
set +e
npx stryker run "$TEMP_CONFIG"
EXIT_CODE=$?
set -e

# ─── Cleanup temp config (node_modules restored by trap on EXIT) ──────────────
rm -f "$TEMP_CONFIG"

# ─── Final report ─────────────────────────────────────────────────────────────
echo ""
if [ "$EXIT_CODE" -eq 0 ]; then
  echo "✓ Mutation testing complete."
  echo "  Report: ${REPORT_DIR}/mutation-$(basename "$MODULE" | sed 's/\..*//'  ).json"
  echo ""
  echo "Next: use the run-mutation-test skill to interpret any surviving mutants."
else
  echo "Mutation testing finished with survivors or errors."
  echo "  Report: ${REPORT_DIR}/"
  echo ""
  echo "Next: use the run-mutation-test skill to interpret surviving mutants."
fi

exit "$EXIT_CODE"
